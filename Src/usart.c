static void UsartDmaRxConfig(USART_TypeDef *USARTx, uint8_t *pRxBuffer,
                             uint32_t rxBufferLength)
{
    DMA_TypeDef *DMAx = 0;
    uint32_t LL_DMA_STREAM_x = 0;

    if (USARTx == USART1)
    {
        DMAx = DMA2;
        LL_DMA_STREAM_x = LL_DMA_STREAM_2;
    }
    else if (USARTx == USART2)
    {
        DMAx = DMA1;
        LL_DMA_STREAM_x = LL_DMA_STREAM_5;
    }
    else if (USARTx == USART3)
    {
        DMAx = DMA1;
        LL_DMA_STREAM_x = LL_DMA_STREAM_1;
    }
    else if (USARTx == UART4)
    {
        DMAx = DMA1;
        LL_DMA_STREAM_x = LL_DMA_STREAM_2;
    }
    else if (USARTx == UART5)
    {
        DMAx = DMA1;
        LL_DMA_STREAM_x = LL_DMA_STREAM_0;
    }
    else if (USARTx == USART6)
    {
        DMAx = DMA2;
        LL_DMA_STREAM_x = LL_DMA_STREAM_1;
    }
    else
    {
        return;
    }

    LL_DMA_DisableStream(DMAx, LL_DMA_STREAM_x);
    LL_DMA_SetPeriphAddress(DMAx, LL_DMA_STREAM_x,
                            LL_USART_DMA_GetRegAddr(USARTx));
    LL_DMA_SetMemoryAddress(DMAx, LL_DMA_STREAM_x, (uint32_t)(pRxBuffer));
    LL_DMA_SetDataLength(DMAx, LL_DMA_STREAM_x, rxBufferLength);
    LL_DMA_EnableStream(DMAx, LL_DMA_STREAM_x);
    LL_USART_ClearFlag_IDLE(USARTx);
    LL_USART_EnableIT_IDLE(USARTx);
    LL_USART_EnableDMAReq_RX(USARTx);
}


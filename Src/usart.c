/**
 * @brief Usart dma transmit data
 * @param USARTx, USART1 USART2 USART3
 * @param pdata, point to the buffer to be send
 * @param length, length of the buffer to be send
 */
void Usart_Dma_Transmit(USART_TypeDef *USARTx, uint8_t *pdata, uint32_t length)
{
    while (!LL_USART_IsActiveFlag_TC(USARTx) ||
           !LL_USART_IsActiveFlag_TXE(USARTx))
        ;
    DMA_TypeDef *DMAx = 0;
    uint32_t LL_DMA_STREAM_x = 0;
    if (USARTx == USART1)
    {
        DMAx = DMA2;
        LL_DMA_STREAM_x = LL_DMA_STREAM_7;
    }
    else if (USARTx == USART2)
    {
        DMAx = DMA1;
        LL_DMA_STREAM_x = LL_DMA_STREAM_6;
    }
    else if (USARTx == USART3)
    {
        DMAx = DMA1;
        LL_DMA_STREAM_x = LL_DMA_STREAM_3;
    }
    else if (USARTx == UART4)
    {
        DMAx = DMA1;
        LL_DMA_STREAM_x = LL_DMA_STREAM_4;
    }
    else if (USARTx == UART5)
    {
        DMAx = DMA1;
        LL_DMA_STREAM_x = LL_DMA_STREAM_7;
    }
    else if (USARTx == USART6)
    {
        DMAx = DMA2;
        LL_DMA_STREAM_x = LL_DMA_STREAM_6;
    }
    else
    {
        return;
    }

    LL_DMA_DisableStream(DMAx, LL_DMA_STREAM_x);
    LL_DMA_SetMemoryAddress(DMAx, LL_DMA_STREAM_x, (uint32_t)pdata);
    LL_DMA_SetDataLength(DMAx, LL_DMA_STREAM_x, length);
    LL_DMA_EnableStream(DMAx, LL_DMA_STREAM_x);
    LL_USART_EnableDMAReq_TX(USARTx);
}

